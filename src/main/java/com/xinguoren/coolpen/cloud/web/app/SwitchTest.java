public class SwitchTest {

	public static void main(String[] args) {
		int i = 0;
		switch (i) {
		case 0:
			System.out.println("0");
		case 1:
			System.out.println("1");
		case 2:
			System.out.println("2");

		default:
			System.out.println("default");
		}

	}

}
